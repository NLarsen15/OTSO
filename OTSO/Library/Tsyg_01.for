        IF (MODE.EQ.1) CALL RMCO_TWOCONES (A21,XS,Ysc,ZS,BXS,BYAS,BZS)
        IF (MODE.EQ.2) CALL RMCO_TWOCONES (A22,XS,Ysc,ZS,BXS,BYAS,BZS)
      ENDIF

