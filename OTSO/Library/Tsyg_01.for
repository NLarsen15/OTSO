c      PARMOD(2)=DST
c      PARMOD(3)=BYIMF
c      PARMOD(4)=BZIMF
c      PARMOD(5)=G1
c      PARMOD(6)=G2

c      CALL RMCO_T01_01 (0,PARMOD,PS,XGSM,YGSM,ZGSM,FXGSM,FYGSM,FZGSM)

